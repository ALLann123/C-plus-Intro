#include <iostream>

int main()
{
    /*
    For...Loop = Executes a block of code a specified number of
                times.
    for(index;condition;increment/decrement){}
    */
    for (int i = 10; i >= 0; i--)
    {
        std::cout << i << std::endl;
    }
    std::cout << "Happy New Year!!\n";

    return 0;
}